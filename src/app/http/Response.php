<?php 

declare(strict_types=1);

namespace src\app\http;

class Response 
{   
    
    // ********************************************************************************************
    // ********************************************************************************************
    
    public function view(string $viewName, array $viewData = []) : void
    {
        extract($viewData);
        if(file_exists(__DIR__ . "/../../views/" . $viewName . ".php"))
        {
            require_once __DIR__ . "/../../views/" . $viewName . ".php";
        }
    }

    // ********************************************************************************************
    // ********************************************************************************************
    
    public function publicView(string $viewName, array $viewData = []) : void
    {
        $this->view('public/' . $viewName, $viewData);
    }

    // ********************************************************************************************
    // ********************************************************************************************
    
    public function adminView(string $viewName, array $viewData = []) : void
    {
        $this->view('admin/' . $viewName, $viewData);
    }
    
    // ********************************************************************************************
    // ********************************************************************************************

    public function redirect(string $route) : void
    {
        header("Location:" . $route);
        exit;
    }

    // ********************************************************************************************
    // ********************************************************************************************

    public function json(array $data) : void
    {        
        echo json_encode($data);
    }

    // ********************************************************************************************
    // ********************************************************************************************
}