<?php

declare(strict_types=1);

namespace src\app\http;

class Response
{
    // ********************************************************************************************
    // ********************************************************************************************

    public static function json(array $data = []): void
    {
        if (!empty($data)) {
            header('Content-Type: application/json');
            http_response_code($data['statusCode'] ?? 200);
            echo json_encode($data);
            exit;
        }
    }

    // ********************************************************************************************
    // ********************************************************************************************
}
